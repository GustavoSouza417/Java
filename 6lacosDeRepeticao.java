public class ContagemRegressiva {
    public static void main(String[] args){
        int i = 99;
        
        while(i >= 66){
            System.out.println(i);
            i--;
        }
        
        do{
            System.out.println(i);
            i--;
        } while(i >= 33);
        
        for(i = 32; i >= 0; i--)
            System.out.println(i);
    }
}
